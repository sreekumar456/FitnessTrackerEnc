package com.ft.model;

public class Crypto {
	
	private String inputEncrypt;
	
	private String encryptedMessage;
	
	private String inputDecrypt;
	
	private String decrytedMessage;

	public String getInputEncrypt() {
		return inputEncrypt;
	}

	public void setInputEncrypt(String inputEncrypt) {
		this.inputEncrypt = inputEncrypt;
	}

	public String getEncryptedMessage() {
		return encryptedMessage;
	}

	public void setEncryptedMessage(String encryptedMessage) {
		this.encryptedMessage = encryptedMessage;
	}

	public String getInputDecrypt() {
		return inputDecrypt;
	}

	public void setInputDecrypt(String inputDecrypt) {
		this.inputDecrypt = inputDecrypt;
	}

	public String getDecrytedMessage() {
		return decrytedMessage;
	}

	public void setDecrytedMessage(String decrytedMessage) {
		this.decrytedMessage = decrytedMessage;
	}

}
